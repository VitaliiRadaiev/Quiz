<script>
        window.addEventListener('DOMContentLoaded', () => {
            (function scrollToInit() {
                let triggers = document.querySelectorAll('._scroll-to');
                if (triggers.length) {
                    triggers.forEach(trigger => {
                        let id = trigger.getAttribute('href').match(/#.+$/gi).join('').replace('#', '');
                        let $el = document.getElementById(id);

                        trigger.addEventListener('click', (e) => {
                            e.preventDefault();
                            window.scrollTo({
                                top: $el.offsetTop,
                                behavior: 'smooth'
                            })
                        })
                    })
                }
            })();

            jQuery('.watch-video-btn').click(function(event) {
                if (jQuery(this).data('vimeo')) {
                    var iframe = '<iframe src="https://player.vimeo.com/video/' + jQuery(this).data('vimeo') + '?autoplay=1" frameborder="0" allow="autoplay; fullscreen" allowfullscreen ></iframe>';
                    jQuery('.testi-popup').find('iframe').remove();
                    jQuery('.testi-popup').addClass('shown');
                    jQuery('.testi-popup-inner').append(iframe);
                }
            });

            const popupLinks = document.querySelectorAll('.popup-link');
            const body = document.querySelector('body');
            const lockPadding = document.querySelectorAll('.lock-padding');

            let unlock = true;

            const timeout = 800;

            function openQuizPopup(id) {
                let $el = document.querySelector(`${id}`);
                if ($el) {
                    popupOpen($el)
                } else {
                    throw new Error('Element not found');
                }
            }
            // example
            //openQuizPopup('#popupMessage');

            if (popupLinks.length > 0) {
                for (let index = 0; index < popupLinks.length; index++) {
                    const popupLink = popupLinks[index];
                    if (popupLink) {
                        popupLink.addEventListener('click', function(e) {
                            const popupName = popupLink.getAttribute('href').replace('#', '');
                            const curentPopup = document.getElementById(popupName);
                            popupOpen(curentPopup);
                            e.preventDefault();
                        });
                    }

                }
            }


            const popupCloseIcon = document.querySelectorAll('.close-popup');
            if (popupCloseIcon.length > 0) {
                for (let index = 0; index < popupCloseIcon.length; index++) {
                    const el = popupCloseIcon[index];
                    el.addEventListener('click', function(e) {
                        popupClose(el.closest('.quiz-home-popup'));
                        e.preventDefault();
                    });
                }
            }

            function popupOpen(curentPopup) {
                if (curentPopup && unlock) {
                    const popupActive = document.querySelector('.quiz-home-popup.open');
                    if (popupActive) {
                        popupClose(popupActive, false);
                    } else {
                        bodyLock();
                    }
                    curentPopup.classList.add('open');
                    curentPopup.addEventListener('click', function(e) {
                        if (!e.target.closest('.quiz-home-popup_content')) {
                            popupClose(e.target.closest('.quiz-home-popup'));
                        }
                    });

                }
            }

            function popupClose(popupActive, doUnlock = true) {
                if (unlock) {
                    popupActive.classList.remove('open');
                    if (doUnlock) {
                        bodyUnlock();
                    }
                }
            }

            function bodyLock() {
                const lockPaddingValue = window.innerWidth - document.querySelector('body').offsetWidth + 'px';
                let targetPadding = document.querySelectorAll('._lp');
                if (targetPadding.length) {
                    for (let index = 0; index < targetPadding.length; index++) {
                        const el = targetPadding[index];
                        el.style.paddingRight = lockPaddingValue;
                    }
                }

                if (lockPadding.length > 0) {
                    for (let index = 0; index < lockPadding.length; index++) {
                        const el = lockPadding[index];
                        el.style.paddingRight = lockPaddingValue;
                    }
                }

                body.style.paddingRight = lockPaddingValue;
                body.classList.add('lock');

                unlock = false;
                setTimeout(function() {
                    unlock = true;
                }, timeout);
            }

            function bodyUnlock() {
                let targetPadding = document.querySelectorAll('._lp');

                setTimeout(function() {
                    if (targetPadding.length) {
                        for (let index = 0; index < targetPadding.length; index++) {
                            const el = targetPadding[index];
                            el.style.paddingRight = '0px';
                        }
                    }

                    for (let index = 0; index < lockPadding.length; index++) {
                        const el = lockPadding[index];
                        el.style.paddingRight = '0px';
                    }

                    body.style.paddingRight = '0px';
                    body.classList.remove('lock');
                }, timeout);

                unlock = false;
                setTimeout(function() {
                    unlock = true;
                }, timeout);
            }

            document.addEventListener('keydown', function(e) {
                if (e.which === 27) {
                    const popupActive = document.querySelector('.quiz-home-popup.open');
                    popupClose(popupActive);
                }
            });

            // === Polyfill ===
            (function() {
                if (!Element.prototype.closest) {
                    Element.prototype.closest = function(css) {
                        var node = this;
                        while (node) {
                            if (node.matches(css)) return node;
                            else node == node.parentElement;
                        }
                        return null;
                    };
                }
            })();

            (function() {
                if (!Element.prototype.matches) {
                    Element.prototype.matches = Element.prototype.matchesSelector ||
                        Element.prototype.webkitMatchesSelector ||
                        Element.prototype.mozMatchesSelector ||
                        Element.prototype.mozMatchesSelector;
                }
            })();
            // === AND Polyfill ===


        });
    </script>


